package abreviaturas;

/**
 *
 * @author Arthur
 */
public class Abreviatura {
    
    private String abv;
    private String normal;

    public Abreviatura(String abv, String normal) {
        this.abv = abv;
        this.normal = normal;
    }

    public String getAbv() {
        return abv;
    }

    public void setAbv(String abv) {
        this.abv = abv;
    }

    public String getNormal() {
        return normal;
    }

    public void setNormal(String normal) {
        this.normal = normal;
    }
    
    
}
