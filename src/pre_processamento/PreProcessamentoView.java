    private void jXBPreProcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jXBPreProcessarActionPerformed
        try {
            ArrayList<String> tweetsPreProcessados = PreProcessamento.getINSTANCIA().preProcessar(PreProcessamento.getINSTANCIA().getArquivo(), jRBRemDuplicados.isSelected(), jRBRemRT.isSelected(), tableTermos.getTermos());
            PreProcessamento.getINSTANCIA().salvarTweetsPreProcessados(tweetsPreProcessados);
            JOptionPane.showMessageDialog(null, "Arquivo salvo: Dados pré-processados com sucesso!", "Informação do sistema", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(PreProcessamentoView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        this.dispose();
    }//GEN-LAST:event_jXBPreProcessarActionPerformed

    private void verificarNumeroCaracteresRestantes() {
        String termo = jTFTermo.getText();
        int tam = termo.length();
        int restante = 140 - tam;
        jLNumChar.setText(String.valueOf(restante));
    }

