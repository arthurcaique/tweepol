    private void jTFTermoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFTermoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            jTFTermo.requestFocus();
            adicionarTermo(jTFTermo.getText());
            jTFTermo.setText("");
        }
    }//GEN-LAST:event_jTFTermoKeyPressed

    private void jTFTermoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFTermoKeyReleased
        verificarNumeroCaracteresRestantes();
    }//GEN-LAST:event_jTFTermoKeyReleased

    private void jXTTermosKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jXTTermosKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
            Object[] options = new Object[]{"Sim", "Não"};
            int r = JOptionPane.showOptionDialog(null, "Deseja excluir o termo " + tableTermos.getTermo(jXTTermos.getSelectedRow()) + "?", "Confirmação",
                    JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, options, 0);
            if (r == 0) {
                tableTermos.removeRow(jXTTermos.getSelectedRow());
            }
        }
    }//GEN-LAST:event_jXTTermosKeyPressed

